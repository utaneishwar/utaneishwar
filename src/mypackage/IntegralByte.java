package mypackage;

public class IntegralByte {

	public static void main(String[] args) {
// byte  variablename = value; the size of byte data type is 1 byte = 8 bit
		 // +127/-128
	                
		            byte  b = 12;
		
		            byte b1 = 127;
		            byte b2 = 126;
		            byte b3 = -128;
		          System.out.println(b3);
		          
		          
 // short variablename = value ; the size of short data type  is 2 byte =16 bit
	    //+32767/-32768
		           short s = 123;
		           short s1= 32767;
		           short s2=-32768;
		           System.out.println(s1);
		           
 // int variablename = value; the size of int data type is 4 byte = 32 bit
         // +2147483647/-2147483647
		           int i = 125469;
		           int i1 = 2147483647;
		           int i2 =-2147483648;
		           System.out.println(i);
		           
// long variablename = value ; the size of long data type is 8 byte =64 bit
		  //+645454664546l(here suffix L is mandatory)/-323123213213
		           long l = 645454664546l;
		           long l1= -32312321321312132l;
		           System.out.println(l1);
		           
 //  -------------------------------------------------------------------------------------------------------    		   ;
		           
	//2 float variablename= value; the size of float (floating)data typetype is 4 byte= 32 bit,value in decimal point
		  //min 1.4*10^-45/max 3.4*10^38(here suffix f is mandatory )
		               float f = 56.23f;
		               float f1=1213.235f;
		               float f2= 123564879f;
		               System.out.println(f1);
		               
// double variablename = value; the size of double (floating) data type is 8 byte (value in decimal point)
		  //min 4.9*10^-324/max 1.7*10^308
		               double d = 12131.6549879;
		               double d1 = 1232161.121313;
		               double d2 = 789546422.231456;
		               System.out.println(d2);
		               
		               
		               
		               
		           
		         
	}

}
