package arraysDiscussion;

public class SwapNumber {

	public static void main(String[] args)
	{
         int a = 10;
         int b = 20;
         int c = a;
         
         a=b;                //  20=20
         b=c;                //   10=10
         
         System.out.println(a);  // 20
	
         System.out.println(b);   // 10
 
	}

}
