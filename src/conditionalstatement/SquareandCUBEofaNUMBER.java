package conditionalstatement;

public class SquareandCUBEofaNUMBER
{// Ass no - 9 
	// WAP to get the Square and cube of a number 
	 
  public static int m1(int a)
  {
	int b = a*a;  
	 return b ; 
	  
  }
  public static int m2(int b)
  {
	 int c = b*b*b ;
	 return c ; 
	  
  }
	
	
	public static void main(String[] args)
	{
		System.out.println(" Square is = " + m1(5));
		
		System.out.println(" CUBE is = " + m2(9));
		

	}

}
