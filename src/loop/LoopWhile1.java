package loop;

public class LoopWhile1 {
	// There are three types 
	//   1.while 
	//   2.do while
	//   3.for loop
	 public static void main(String[] args) 
	{
	// by using while a loop we can define or execute 	
	// certain set of code multiple times as per our requirement 	
	 
		 //   syntax:
		 //   while (boolean_condition)
		//     {
		//	    action  
	    //      }
		
	int a = 10;
	while (a<15)
	{
		System.out.println( " hello " );
		
		a++;
	}

	}

}
