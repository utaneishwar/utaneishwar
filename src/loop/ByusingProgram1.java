	public static void main(String[] args)
   {
		// WAP to print ***** 5 times  in line by line.by using single star.
		
		for (int b = 1; b<=5; b++)       // for print 5 times
		{
		
		for (int a = 1; a<=5; a++)    
		{
			System.out.print( " * ");  // using single star and use "print" for single in  line 
			
		}                               // this line print after completion of above statement
           System.out.println( );    // for print line in line 
	}

}
