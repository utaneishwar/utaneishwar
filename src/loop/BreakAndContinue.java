package loop;

public class BreakAndContinue 
{

	public static void main(String[] args)
	{
		// BREAK: it is a keyword which is terminate the program and executing the Statement 
		
		//whenever we want to required break in loop then we should go for " BREAK"
		// BREAK is applicable for all type of loop 
		
		for (int i=0; i<5; i++)
		
		{
		if(i==3)
		{
			break;
		}
		
		System.out.println(i);
		}
	}
}
